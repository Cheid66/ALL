print("Hello, " + a)
print(a[0])
print(a[-1])
print(a[0:3]) #так обозначаем начало и конец отрезка строки, конечное число не входит в отрезок
print(a[0:3:2]) #Выбор элементов строки с определенным шагом
print(a[:3]) #если начало 1ый символ, его можно не указывать, то же самое с последним отрезком - не указываем, если оканчивается на последний символ
print(a[::-1])
